    override func prepare(for segue: UIStoryboardSegue, sender: Any?){
        if segue.identifier == "websegue"{
            if let indexPath = self.tableView.indexPathForSelectedRow{
                let url = animeData.getURL(index: indexPath.row)
                let title = anime[indexPath.row]
                let webVC = (segue.destination as! UINavigationController).topViewController as! SecondaryViewController
                webVC.webpage = url
                webVC.title = title
            }
        }
    }

